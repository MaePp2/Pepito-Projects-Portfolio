    private void jLabel10MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel10MousePressed
        // TODO add your handling code here:
        // TODO add your handling code here:
        // BUTTON FOR UPDATING EQUIPMENT INFORMATION
        IFactory conditionFactory = new ConditionFactory();
        ICondition condition = conditionFactory.doSomething(jComboBox1.getSelectedIndex());

        if(checkInputs() && jTextField1.getText() != null)
        {
            try {
                Equipment previousRecord = jdbc.getTableRow(tableName, Integer.parseInt(jTextField1.getText()));
                quantity = Integer.parseInt(jTextField3.getText());

                jdbc.update(tableName, Integer.parseInt(jTextField1.getText()), jTextField2.getText(), condition.conditionName(), jTextField3.getText());

                if(previousRecord.getAssetcondition() != "For Replacement" && jComboBox1.getSelectedItem().equals("Lost/Disposed equipment")){
                    price = Integer.parseInt(jTextField4.getText());
                    totalPrice = quantity * price;
                    jdbc.insertPurchases(tableName, String.valueOf(totalPrice), Integer.parseInt(jTextField1.getText()));
                }
                else if(jComboBox1.getSelectedItem().equals("Lost/Disposed equipment")){
                    price = Integer.parseInt(jTextField4.getText());
                    totalPrice = quantity * price;
                    jdbc.updatePurchases(tableName, Integer.parseInt(jTextField1.getText()), jTextField2.getText(), condition.conditionName(), jTextField3.getText(), totalPrice);
                }
                else{
                    jdbc.delete(tableName + "_purchasesList", Integer.parseInt(jTextField1.getText()));
                }

                   ImageIcon bell = new ImageIcon ("src/Images/icons8_bell_64px.png");
                   JOptionPane.showMessageDialog(this, "EQUIPMENT UPDATED", "SUCCESSFUL", JOptionPane.INFORMATION_MESSAGE, bell);
                ShowEquipmentTable();
            } catch (SQLException ex) {
                Logger.getLogger(AddEquipment.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AddEquipment.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else{
                   ImageIcon bell = new ImageIcon ("src/Images/icons8_private_64px.png");
                   JOptionPane.showMessageDialog(this, "YOU MUST COMPLETE ALL THE FORMS.", "ERROR", JOptionPane.ERROR_MESSAGE, bell);
        }
    }//GEN-LAST:event_jLabel10MousePressed

