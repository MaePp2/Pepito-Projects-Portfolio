    public void ShowEquipmentTable() throws SQLException, ClassNotFoundException
    {
        ArrayList<Equipment> list = jdbc.getTableList(tableName);
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        // clear jtable content
        model.setRowCount(0);
        Object[] row = new Object[4];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getName();
            row[2] = list.get(i).getAssetcondition();
            row[3] = list.get(i).getQuantity();
            
            model.addRow(row);
        }
    }
    
    public void ShowPurchasesTable() throws SQLException, ClassNotFoundException
    {
        ArrayList<DamageReportDetails> list = jdbc.getPurchasesList(tableName);
        DefaultTableModel model = (DefaultTableModel)purchasesTable.getjTable1().getModel();
        // clear jtable content
        model.setRowCount(0);
        Object[] row = new Object[5];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getName();
            row[2] = list.get(i).getAssetcondition();
            row[3] = list.get(i).getQuantity();
            row[4] = list.get(i).getPrice();
            
            model.addRow(row);
        }
    }
    
