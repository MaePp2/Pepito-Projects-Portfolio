    private void jLabel13MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel13MousePressed
        // TODO add your handling code here:
        // TODO add your handling code here:
        // BUTTON FOR ADDING EQUIPMENT TO THE LIST
        IFactory conditionFactory = new ConditionFactory();
        ICondition condition = conditionFactory.doSomething(jComboBox1.getSelectedIndex());
        int ID = 0;
        quantity = Integer.parseInt(jTextField3.getText());

        if(checkInputs())
        {
            try {
                count = jdbc.countRows(tableName);
                if (count < 5) {

                    ID = jdbc.insert(tableName, jTextField2.getText(), condition.conditionName(), jTextField3.getText());

                    if(jComboBox1.getSelectedItem().equals("Lost/Disposed equipment")){
                        price = Integer.parseInt(jTextField4.getText());
                        totalPrice = quantity * price;
                        jdbc.insertPurchases(tableName, String.valueOf(totalPrice), ID);
                    }

                   ImageIcon bell = new ImageIcon ("src/Images/icons8_bell_64px.png");
                   JOptionPane.showMessageDialog(this, "EQUIPMENT ADDED", "SUCCESSFUL", JOptionPane.INFORMATION_MESSAGE, bell);
                    ShowEquipmentTable();
                }
                else {
                   ImageIcon bell = new ImageIcon ("src/Images/icons8_private_64px.png");
                   JOptionPane.showMessageDialog(this, "YOU CAN ONLY ADD 5 EQUIPMENT AT A TIME", "STOP", JOptionPane.ERROR_MESSAGE, bell);
                }
            } catch (Exception ex) {
                   ImageIcon bell = new ImageIcon ("src/Images/icons8_unavailable_64px_1.png");
                   JOptionPane.showMessageDialog(this, "COULD NOT ADD EQUIPMENT.", "ERROR", JOptionPane.ERROR_MESSAGE, bell);
            }
        }
        else
        {
                   ImageIcon bell = new ImageIcon ("src/Images/icons8_private_64px.png");
                   JOptionPane.showMessageDialog(this, "YOU MUST COMPLETE ALL THE FORMS.", "ERROR", JOptionPane.ERROR_MESSAGE, bell);
        }
    }//GEN-LAST:event_jLabel13MousePressed

